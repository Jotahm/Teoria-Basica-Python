# Los operadores son los distintos signos que el intérprete utiliza para hacer una operación específica

# + - * / % (Al hacer una división / nos devuelve un número flotante. Para que nos devuelva un entero, utilizamos //. Ejemplo 10/2=5.0  ; 10//2=5)
# ** exponente

# El orden de precedencia es similar al matemático (Paréntesis-exponentes-multiplicación-división-suma-resta)

numero1 = 10
numero2 = 5

print (numero1 + numero2)
print (numero1 * numero2)
print (numero1 / numero2)
print (numero1 // numero2)

print (float (numero1))

